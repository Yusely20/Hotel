    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Categoria categoria;
        switch(Integer.parseInt(Cate.getText())){
            case 1:
            categoria = Categoria.LIMPIEZA;
            break;
            case 2:
            categoria = Categoria.ROPA_CAMA;
            break;
            case 3:
            categoria = Categoria.ACCESORIO_BANIO;
            break;
            case 4:
            categoria = Categoria.ALIMENTO;
            break;
            default:
            categoria = Categoria.NA;
            break;
        }
        int cantidad = Integer.parseInt(Cant.getText());

        hotel.agregarArticulo(Nom.getText(), categoria, cantidad, alerta(cantidad));
        this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

